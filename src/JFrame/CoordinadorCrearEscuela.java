private void Validar() {
        try {
            if (!txtDirector.getText().isEmpty()) {
                if(!txt_nombreEscuela.getText().isEmpty()){
                    System.out.println("Ingresado");
                }else{
                    JOptionPane.showMessageDialog(this, "Ingrese el nombre de la escuela", "Error!", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese al director", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }

