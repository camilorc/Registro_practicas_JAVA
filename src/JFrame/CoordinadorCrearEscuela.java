    private void jb_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_crearActionPerformed
       Validar();
        try {
            EscuelaDAO escuela = new EscuelaDAO();
            //Obtenemos atributos del formulario}
            String nombreEscuela = txt_nombreEscuela.getText();
            String director= txtDirector.getText();
            if (escuela.Create(nombreEscuela, director)) {
                JOptionPane.showMessageDialog(null,"Nueva escuela creada", "Atencion", JOptionPane.INFORMATION_MESSAGE);
                Limpiar();
            } else {
                JOptionPane.showMessageDialog(null,"Error al crear escuela "+ nombreEscuela, "Error",JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, null, "Escuela no pudo ser creada"+e.getMessage(), JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jb_crearActionPerformed

