    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Validar();
        // Llamar al APDEIT de EscuelaDAO
        CarreraDAO sedecita = new CarreraDAO();
        recargar();
        int cable = ca.getIdCarrera();
        if (sedecita.Update(cable, txt_nombre.getText(), 1,
                cmb_Escuela.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(null, "Actualizado ", "OEEEE", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar. ", "NOOOOO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void recargar() {
        int cable = ca.getIdCarrera();
        txt_nombre.setText(ca.getNombreCarrera());
        cmb_Escuela.setSelectedItem(ca.getNombreEscuela());
    }

