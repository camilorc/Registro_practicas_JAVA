    private void iniciarTabla() {
        ListaAlumnos li = new ListaAlumnos();
        setLocationRelativeTo(null);
        modelo = (DefaultTableModel)jtableuno.getModel();
        ArrayList<ListaAlumnos>lista = li.listarAsignaciones();
        Object[] fila = new Object[modelo.getColumnCount()];
        System.out.println("tamaño lista: "+lista.size());
        for (int i = 0; i < lista.size(); i++) {
            fila[0] = lista.get(i).getRut()+"-"+lista.get(i).getDv();
            fila[1] = lista.get(i).getNombres()+" "+lista.get(i).getApellido1();
            fila[2] = lista.get(i).getNombre_docente();
            fila[3] = lista.get(i).getEstado_detalle();
            modelo.addRow(fila);
        }
    }

    private void llenarComboBox() {
        Usuario user = new Usuario();
        ArrayList<Usuario> docentes = user.BuscarTipoUsuario(3);

        for (Usuario docente : docentes) {
            jcb_docentes.addItem(docente);
        }
    }

    private void limpiarTabla() {
        
        try{
            modelo = (DefaultTableModel) jtableuno.getModel();
            
            while(modelo.getRowCount() > 0){
                for (int i = 0; i < modelo.getRowCount(); i++) {
                    modelo.removeRow(i);
                }
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }
    
