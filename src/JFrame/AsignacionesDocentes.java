    private void btn_asignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_asignarActionPerformed
        // TODO add your handling code here:
        //Comprobamos que tenga seleccionado algo de la tabla
        if(jtableuno.getSelectedRow() != -1){
            int row = jtableuno.getSelectedRow();
            String rut_alumno = jtableuno.getValueAt(row, 0).toString();
            String[] rutArray = rut_alumno.split("-");
            
            int rut = Integer.parseInt(rutArray[0]);
            
            Usuario user = new Usuario();
            user = (Usuario)jcb_docentes.getSelectedItem();
            user.CambiarDocenteAsignado(rut,user.getRut());
            limpiarTabla();
            iniciarTabla();
            JOptionPane.showMessageDialog(null, "Asignación cambiada", "Exito", JOptionPane.DEFAULT_OPTION);
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar para asignar", "ERROR", JOptionPane.ERROR_MESSAGE);
        
        }
        
        
    }//GEN-LAST:event_btn_asignarActionPerformed

