    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String nombreEscuelaAEliminar = cmb_Nombre.getSelectedItem().toString();
        EscuelaDAO esc = new EscuelaDAO();
        Escuela escuela = esc.BuscarPorNombreDeEscuela(nombreEscuelaAEliminar);
        if (esc.Delete(nombreEscuelaAEliminar)){
            JOptionPane.showMessageDialog(null, "Escuela "+ nombreEscuelaAEliminar + " eliminada", "Alerta", JOptionPane.INFORMATION_MESSAGE);
            actualizarComboBox(esc.BuscarTodasLasEscuelas());
        }
        else{
            JOptionPane.showMessageDialog(null, "Escuela "+ nombreEscuelaAEliminar + " no pudo ser eliminada", "Alerta", JOptionPane.ERROR_MESSAGE);
        }
        
        actualizarComboBox(esc.BuscarTodasLasEscuelas());
        
    }//GEN-LAST:event_btnEliminarActionPerformed

