    private void actualizarComboBox(ArrayList<Escuela> escuelas){
        cmb_Nombre.removeAll();
        cmb_Nombre.removeAllItems();
        
        for(Escuela e : escuelas){
            cmb_Nombre.addItem(e.getNombreEscuela());
        }
    }

